add = lambda x , y : x + y
print("Addition is :",add(5,3))

sub = lambda x , y : x - y
print("Subtraction is :",sub(5,3))

mul = lambda x , y : x * y
print("Multiplication is :",mul(3,3))

div = lambda x , y : x / y
print("Division is :",div(6,3))

floor_division = lambda x , y : x // y
print("Floor division  is :",floor_division(5,3))

mod = lambda x , y : x % y
print("Modulus is :",mod(5,3))

expo = lambda x , y : x ** y
print("Exponent is :",expo(5,3))



#OUTPUT:
# Addition is : 8
# Subtraction is : 2
# Multiplication is : 9
# Division is : 2.0
